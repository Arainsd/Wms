using System;

namespace TygaSoft.Model
{
    [Serializable]
    public partial class CustomerInfo
    {
        public CustomerInfo() { }

        public CustomerInfo(Guid id, Guid userId, string coded, string named, string shortName, string contactMan, string email, string phone, string telPhone, string fax, string postcode, string address, string remark, DateTime lastUpdatedDate)
        {
            this.Id = id;
            this.UserId = userId;
            this.Coded = coded;
            this.Named = named;
            this.ShortName = shortName;
            this.ContactMan = contactMan;
            this.Email = email;
            this.Phone = phone;
            this.TelPhone = telPhone;
            this.Fax = fax;
            this.Postcode = postcode;
            this.Address = address;
            this.Remark = remark;
            this.LastUpdatedDate = lastUpdatedDate;
        }

        public Guid Id { get; set; }
        public Guid UserId { get; set; }
        public string Coded { get; set; }
        public string Named { get; set; }
        public string ShortName { get; set; }
        public string ContactMan { get; set; }
        public string Email { get; set; }
        public string Phone { get; set; }
        public string TelPhone { get; set; }
        public string Fax { get; set; }
        public string Postcode { get; set; }
        public string Address { get; set; }
        public string Remark { get; set; }
        public DateTime LastUpdatedDate { get; set; }
    }
}
